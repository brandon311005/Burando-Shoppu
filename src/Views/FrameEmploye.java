    private void btnmodifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifierActionPerformed
        
         try {
             // TODO add your handling code here:

             Conn = (Connection) dataBase.getConnection();
             PreparedStatement mystm  = Conn.prepareStatement("update  Employees set NomEmploy=?,PrenomEmploy=?,Role=?"+"where Login=? or PassWord=?");
             
             String log=txtlogin.getText();
             String nom=txtnom.getText();
             String prenom=txtprenom.getText();
             String role=txtrole.getText();
             String passw=txtpass.getText();
             Employe c = new Employe(log, nom, prenom, role, passw);
             mystm.setString(4, log);
             mystm.setString(1, nom);
             mystm.setString(2, prenom);
             mystm.setString(3, role);
             mystm.setString(5, passw);
             mystm.executeUpdate();
             Emplo.add(c);
             c.setNom(nom);
             c.setPrenom(prenom);
             c.setRole(role);
             
             JOptionPane.showMessageDialog(null,"Informations de l'employé mis à jour avec succes.");
         } catch (SQLException ex) {
             Logger.getLogger(FrameEmploye.class.getName()).log(Level.SEVERE, null, ex);
         }
        

    }//GEN-LAST:event_btnmodifierActionPerformed

