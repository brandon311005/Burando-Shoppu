    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed
        try {
            // TODO add your handling code here:

            Conn = (Connection) dataBase.getConnection();
            PreparedStatement mystm  = Conn.prepareStatement("insert into Employees values(?,?,?,?,?)");
            String log=txtlogin.getText();
            String nom=txtnom.getText();
            String prenom=txtprenom.getText();
            String role=txtrole.getText();
            String passw=txtpass.getText();
            Employe c = new Employe(log, nom, prenom, role, passw);
            mystm.setString(1, log);
            mystm.setString(2, nom);
            mystm.setString(3, prenom);
            mystm.setString(4, role);
            mystm.setString(5, passw);
            mystm.executeUpdate();
            Emplo.add(c);
            JOptionPane.showMessageDialog(null,"Employé ajouté avec succès.");
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(FrameProduit.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnajouterActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
           String sql1 ="select* from Employees"; 
        try {
             // TODO add your handling code here:
             
             Conn = (Connection) dataBase.getConnection();
             stm=Conn.createStatement();
             rst=stm.executeQuery(sql1);
             while(rst.next())
             {
                 Emplo.add(new Employe(rst.getString(1), rst.getString(2),rst.getString(3),rst.getString(4),rst.getString(5)));
             }
             rst.close();
             Conn.close();
             afficher();
         } catch (SQLException ex) {
             Logger.getLogger(FrameEmploye.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_formWindowOpened

