    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String sql1 ="select* from Produits"; 
        try {
             // TODO add your handling code here:
             Conn = (Connection) dataBase.getConnection();
             stm=Conn.createStatement();
             rst=stm.executeQuery(sql1);
             while(rst.next())
             {
                 Clts.add(new Article(rst.getString(1), rst.getString(2),rst.getInt(3),rst.getInt(4),rst.getString(5)));
             }
             rst.close();
             Conn.close();
             afficher();
         } catch (SQLException ex) {
             Logger.getLogger(FrameProduit.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_formWindowOpened

