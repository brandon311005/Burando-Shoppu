    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed
         try {
             // TODO add your handling code here:
             
             
             Conn = (Connection) dataBase.getConnection();
             PreparedStatement mystm  = Conn.prepareStatement("insert into Produits values(?,?,?,?,?)");
             String id=txtidart.getText();
             String nom=txtnom.getText();
             int prix=Integer.parseInt(txtprix.getText());
             int qte=Integer.parseInt(txtqte.getText());
             String idcart=txtidart.getText();
             Article c = new Article(id, nom, prix, qte, idcart);
             mystm.setString(1, id);
             mystm.setString(2, nom);
             mystm.setFloat(3, prix);
             mystm.setInt(4, qte);
             mystm.setString(5, idcart);
             mystm.executeUpdate();
             Clts.add(c);
             JOptionPane.showMessageDialog(null,"Produit ajouté avec succès.");
         } catch (SQLException ex) {
             java.util.logging.Logger.getLogger(FrameProduit.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
         }

      

    }//GEN-LAST:event_btnajouterActionPerformed

    private void btnmodifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifierActionPerformed
         try {
             // TODO add your handling code here:
             
             
             Conn = (Connection) dataBase.getConnection();
             PreparedStatement mystm  = Conn.prepareStatement("update  Produits set NomProduit=?,PrixUn=?,StockDispo=? where IdProduit=? or IdCategorie=?");
             
             String id=txtidart.getText();
             String nom=txtnom.getText();
             Float prix=Float.parseFloat(txtprix.getText());
             int qte=Integer.parseInt(txtqte.getText());
             String idcart=txtcard.getText();
             Article c = new Article(id, nom, prix, qte, idcart);
             mystm.setString(1, nom);
             mystm.setFloat(2, prix);
             mystm.setInt(3, qte);
             mystm.setString(4, id);
             mystm.setString(5, idcart);
             mystm.executeUpdate();
             Clts.add(c);
             c.setNom(nom);
             c.setPrixUn(prix);
             c.setQte_stock(qte);
             
             JOptionPane.showMessageDialog(null,"Produit modifié avec succès.");
         } catch (SQLException ex) {
             java.util.logging.Logger.getLogger(FrameProduit.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
         }

        

    }//GEN-LAST:event_btnmodifierActionPerformed

